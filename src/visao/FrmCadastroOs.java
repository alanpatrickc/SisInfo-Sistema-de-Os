    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyyy");

        try {
            modeloOs.setDataEntrada(sdf.parse(jTextFieldData.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(FrmCadastroOs.class.getName()).log(Level.SEVERE, null, ex);
        }
        modeloOs.setHoraEntrada(Time.valueOf(jTextFieldHora.getText()));
        modeloOs.setTecResponsavel((String) jComboBoxTecnico.getSelectedItem());
        modeloOs.setSitAtual((String) jComboBoxSituOrdem.getSelectedItem());
        modeloOs.setCliente(jTextFieldCliente.getText());
        modeloOs.setTelefone(jTextFieldTelefone.getText());
        modeloOs.setEquipamento(jTextFieldEqui.getText());
        modeloOs.setNumSerie(jTextFieldSn.getText());
        modeloOs.setModelo(jTextFieldModelo.getText());
        modeloOs.setMarca(jTextFieldMarca.getText());
        modeloOs.setAcessorios(jTextFieldAcessorios.getText());
        modeloOs.setProInformado(jTextFieldDefInfo.getText());
        modeloOs.setProConstatado(jTextFieldProConst.getText());
        modeloOs.setObs(jTextAreaObs.getText());
        modeloOs.setFormPagamento((String) jComboBoxFormPag.getSelectedItem());
        try {
            modeloOs.setDataSaida(sdf.parse(jFormattedTextFieldDataSaida.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(FrmCadastroOs.class.getName()).log(Level.SEVERE, null, ex);
        }
        modeloOs.setValProduto(jNumberFieldValPro.getValue().doubleValue());
        modeloOs.setValDesconto(jNumberFieldValDesc.getValue().doubleValue());
        modeloOs.setValTotal(jNumberFieldValTotal.getValue().doubleValue());

        ControllerOs control = new ControllerOs();
        control.salvarOs(modeloOs); // TODO add your handling code here:
    }//GEN-LAST:event_jButton8ActionPerformed

