    public void preencherTabel(String SQL) {

        ArrayList dados = new ArrayList();

        String[] Colunas = new String[]{"Cliente", "Telefone"};
        conectaOs.conexao();
        conectaOs.executaSQL(SQL);

        try {

            conectaOs.rs.first();
            do {

                dados.add(new Object[]{conectaOs.rs.getString("Servico"), conectaOs.rs.getString("Quantidade"), conectaOs.rs.getString("ValorUnitario"), conectaOs.rs.getString("valor Total")});

            } while (conectaOs.rs.next());

        } catch (SQLException ex) {
//        JOptionPane.showMessageDialog(null,"Sem Registros no banco de dados!");

        }
        ModeloTabe modelo = new ModeloTabe(dados, Colunas);
        jTableServico.setModel(modelo);
        jTableServico.getColumnModel().getColumn(0).setPreferredWidth(200);
        jTableServico.getColumnModel().getColumn(0).setResizable(false);
        jTableServico.getColumnModel().getColumn(1).setPreferredWidth(200);
        jTableServico.getColumnModel().getColumn(1).setResizable(false);

        jTableServico.getTableHeader().setReorderingAllowed(false);
        jTableServico.setAutoResizeMode(jTableServico.AUTO_RESIZE_OFF);
        jTableServico.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        conectaOs.desconecta();
    }

