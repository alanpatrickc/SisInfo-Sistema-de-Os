    private void adicionarServico(){
        
        //Obtém a tabela para trabalhar nela
        tableModel = (DefaultTableModel) jTableServico.getModel();
        //se a quantidade solicitada for maior que valor em estoque
        Integer id;

        //pega numero do ultimo item adicionado
        Integer ultimaLinha = jTableServico.getModel().getRowCount();
        if (ultimaLinha < 1) {
            id = 1;
        } else {
            id = (Integer) jTableServico.getValueAt(ultimaLinha - 1, 0);
            id += 1;
        }

        //Cria array com valores do produto
        Object[] dadosTabela = new Object[5];
        //Cada dado na coluna correspondente
        dadosTabela[0] = id;
        dadosTabela[1] = jTextFieldServico.getText();
        dadosTabela[2] = jTextFieldQuantidade.getText();
        dadosTabela[3] = jTextFieldValUnit.getText();
        dadosTabela[4] = jTextFieldValTot.getText();

        //Adiciona a linha de dados na tabela
        tableModel.addRow(dadosTabela);
            somarValorTotalServicos();   
    }
    
    
    
